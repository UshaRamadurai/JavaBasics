package week1.day2;
//Goal : Basic calculator methods with parameters

public class Calculator {
	
//  addition method for 3 integer numbers with return statement
	public int addition(int num1, int num2, int num3) {
		int sum = num1 + num2 + num3;
		return sum;
	}

//  subtraction method for 2 integer numbers with return statement
	public int subtraction(int num1, int num2) {
		int sub = num1 - num2;
		return sub;
	}

//  multiplication method for 2 double numbers with return statement
	public double multiplication(double num1, double num2) {
		double mul = num1 * num2;
		return mul;
	}

//  division method for 2 float numbers with return statement
	public float division(float num1, float num2) {
		float div = num1 / num2;
		return div;
	}

}
